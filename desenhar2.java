package zaovivo24junho2020triangulo;

public class ContruindoTriangulo {

	public static void main(String[] args) {
		
		int linhas = 10;
		
		for(int i=1; i<=linhas; i++) {
			
			System.out.println("o");
			
			for(int j=1; j<=i; j++) {
				System.out.print(" * ");
			}
			
			//System.out.println("");
			
		}
			
	}
	
}
